package com.entity;

/**
 * @author 高永佳
 */

public class SecondCate {

    private int id;
    private String title;
    private int top_cateid;
    private int level;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getTop_cateid() {
        return top_cateid;
    }

    public void setTop_cateid(int top_cateid) {
        this.top_cateid = top_cateid;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
